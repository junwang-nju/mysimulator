
#ifndef _Intrinsic_Type_Square_H_
#define _Intrinsic_Type_Square_H_

namespace mysimulator {

  long double square(const long double& ld) { return ld*ld; }
  double square(const double& d) { return d*d; }
  float square(const float& f) { return f*f; }
  long long square(const long long& ll) { return ll*ll; }
  unsigned long long square(const unsigned long long& ull) { return ull*ull; }
  int square(const int& i) { return i*i; }
  unsigned int square(const unsigned int& u) { return u*u; }
  long square(const long& l) { return l*l; }
  unsigned long square(const unsigned long& ul) { return ul*ul; }
  short square(const short& s) { return s*s; }
  unsigned short square(const unsigned short& us) { return us*us; }
  char square(const char& c) { return c*c; }
  unsigned char square(const unsigned char& uc) { return uc*uc; }

}

#endif
